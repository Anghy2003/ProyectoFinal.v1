// M칠todo para obtener el pr칩ximo c칩digo de servicio disponible

  private static String obtenerProximoCodigoServicio(ObjectContainer db) {
    // Consultar todos los servicios
    ObjectSet<Servicios> result = db.query(Servicios.class);

    // Determinar el m치ximo ID num칠rico existente
    int masid = 0;
    for (Servicios servicio : result) {
        String codigo = servicio.getCodigo_Servicio();
        if (codigo != null && codigo.startsWith("SER")) {
            int id = Integer.parseInt(codigo.substring(3)); // Extraer la parte num칠rica del c칩digo
            if (id > masid) {
                masid = id;
            }
        }
    }
    // El pr칩ximo ID es el m치ximo + 1
    int siguienteID = masid + 1;
    // Retornar el nuevo c칩digo concatenado con "SER"
    return "SER" + siguienteID;
}

// M칠todo para guardar un nuevo servicio
  public static void guardarServicio(String nombre_Servicio, String descripcion_Servicio, double precioTotal_Servicio, String duracion_Servicio, byte[] imagen, Servicios.Estado estado, String categoria) {
    ObjectContainer baseBD = Conexion_db.ConectarBD();
    String codigo_Servicio = obtenerProximoCodigoServicio(baseBD);

    if (verificarServicio(baseBD, codigo_Servicio) == 0) {
        Servicios nuevoServicio = new Servicios(codigo_Servicio, nombre_Servicio, descripcion_Servicio, precioTotal_Servicio, duracion_Servicio, imagen, Servicios.Estado.ACTIVO, categoria);
        baseBD.set(nuevoServicio);
        JOptionPane.showMessageDialog(null, "Servicio guardado exitosamente.");
    } else {
        JOptionPane.showMessageDialog(null, "El c칩digo de servicio ya existe. Intente nuevamente.");
    }
    baseBD.close();
}
// M칠todo para verificar si un servicio existe
    public static int verificarServicio(ObjectContainer baseBD, String codigo_Servicio) {
        // Crear objeto para buscar el servicio por su c칩digo
        Servicios servicioBusca = new Servicios(codigo_Servicio, null, null, 0, null, null, null,null);
        ObjectSet<Servicios> resultado = baseBD.get(servicioBusca);
        return resultado.size();
    }
    private void btnSeleccionarImgenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarImgenActionPerformed
        JFileChooser jFileChooser = new JFileChooser();
        FileNameExtensionFilter filtrado = new FileNameExtensionFilter("JPG, PNG & GIF", "jpg", "png", "gif");
        jFileChooser.setFileFilter(filtrado);

        int respuesta = jFileChooser.showOpenDialog(this);

        if (respuesta == JFileChooser.APPROVE_OPTION) {
            try {
                BufferedImage bufferedImage = ImageIO.read(jFileChooser.getSelectedFile());
                ByteArrayOutputStream baos = new ByteArrayOutputStream();
                ImageIO.write(bufferedImage, "jpg", baos); 
                imagenServicio = baos.toByteArray();

                Image mImagen = new ImageIcon(imagenServicio).getImage();
                ImageIcon mIcono = new ImageIcon(mImagen.getScaledInstance(lblImagen.getWidth(), lblImagen.getHeight(), Image.SCALE_SMOOTH));
                lblImagen.setIcon(mIcono);
            } catch (IOException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnSeleccionarImgenActionPerformed

