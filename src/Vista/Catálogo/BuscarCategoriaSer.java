 public static void modificarCategoriaServicio(String codigoCat, String nombreCat, String descripcionCat) {
        ObjectContainer BaseBD = Conexion_db.ConectarBD();

        CategoriaServicio categoriaModificada = new CategoriaServicio(codigoCat, nombreCat, descripcionCat);

        CategoriaServicio categoriaBusca = new CategoriaServicio(codigoCat, null, null);
        ObjectSet<CategoriaServicio> resultado = BaseBD.get(categoriaBusca);

        if (resultado.size() > 0) {
            CategoriaServicio categoriaExistente = resultado.next();
            categoriaExistente.setNombreCatSer(nombreCat);
            categoriaExistente.setDescripcionCatSer(descripcionCat);
            BaseBD.store(categoriaExistente);
        } else {
            System.out.println("No se encontró ninguna categoría con el código especificado.");
        }

        BaseBD.close();
    }
public final void buscarCategoriaServicio() {
        Boolean encontrado = false;
        ObjectContainer BaseBD = Conexion_db.ConectarBD();
        Query categoriaQuery = BaseBD.query();
        categoriaQuery.constrain(CategoriaServicio.class);
        categoriaQuery.descend("codigoCatSer").constrain(BuscarCodigo.toUpperCase());
        ObjectSet<CategoriaServicio> resultado = categoriaQuery.execute();

        for (CategoriaServicio cat : resultado) {
            txtCodigo.setText(cat.getCodigoCatSer());
            txtNombres.setText(cat.getNombreCatSer());
            txtDescripcion.setText(cat.getDescripcionCatSer());
            encontrado = true;
            JOptionPane.showMessageDialog(this, "CATEGORÍA ENCONTRADA");
        }
        if (!encontrado) {
            JOptionPane.showMessageDialog(this, "No se encontró la Categoría");
        }

        BaseBD.close();
    }

